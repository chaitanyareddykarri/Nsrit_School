export const hasPermission = (role, permission) =>
  Boolean(ROLE_PERMISSIONS[role]?.includes(permission));

export const canEditAttendance = role =>
  [USER_ROLES.COORDINATOR, USER_ROLES.PRINCIPAL, USER_ROLES.MAIN_ADMIN]
    .includes(String(role || '').toUpperCase());
